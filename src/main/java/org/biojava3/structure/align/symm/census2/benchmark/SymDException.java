package org.biojava3.structure.align.symm.census2.benchmark;

public class SymDException extends Exception {

	private static final long serialVersionUID = 477194555138160170L;

	public SymDException() {
	}

	public SymDException(String message) {
		super(message);
	}

	public SymDException(Throwable cause) {
		super(cause);
	}

	public SymDException(String message, Throwable cause) {
		super(message, cause);
	}

	public SymDException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
